"""
Demonstrates the string type
"""

print("Hello World!")                       #Prints the literal text Hello World!

my_greeting = "Hello World!"                #Declares a variable named my_greeting and assigns it the string literal "Hello World"
print(my_greeting)                          #Prints the value of my_greeting

my_greeting = "Hi there!"                   #Assigns the string literal "Hi There!" to the my_greeting variable
print(my_greeting)                          #Prints the value of my_greeting

"""
Demonstrates copying variables
"""

value1 = 10                        #Declares a variable named value1 and assigns it the value 10

copy_of_value1 = value1            #Copies the reference of value1 to copy_of_value1

print(value1)                      #Prints the value referenced by value1
print(copy_of_value1)              #Prints the value referenced by copy_of_value1


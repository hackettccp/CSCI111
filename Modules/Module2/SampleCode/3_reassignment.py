"""
Demonstrates reassigning new values to variables
"""

current_temperature = 65                    #Declares a variable named current_temperature and assigns it the value 65
print(current_temperature)                  #Prints the value referenced by the variable current_temperature

print()                                     #Prints a blank line

current_temperature = 67                    #Assigns the value 67 to the previously declared variable named current_temperature
print(current_temperature)                  #Prints the value referenced by the variable current_temperature

"""
Demonstrates escape sequences in strings
"""
        
output1 = "First part\nSecond part"                     #Line Feed escape sequence.
print(output1)

output2 = "The book \"War and Peace\" is very long"     #Double quotes escape sequence.
print(output2)

output3 = 'That is Tom\'s bike'                         #Single quote escape sequence.
print(output3)

output4 = "A single backslash \\ will be inserted"      #Backslash escape sequence.
print(output4)

"""
Demonstrates using relative indexes in lists.
"""

directions = ["North", "South", "East", "West"]         #A list named directions containing the values "North", "South", "East" and "West"

print(directions[-1])                                   #Prints the last element in the directions list using a relative (negative) index

print(directions[-4])                                   #Prints the first element in the directions list using a relative (negative) index




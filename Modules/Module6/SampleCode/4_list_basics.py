"""
Demonstrates lists.
"""

numbers = [8, 5, 7]                 #Declares a list named numbers containing the values 8, 5 and 7

print(len(numbers))                 #Prints the length of the numbers list

print(numbers[1])                   #Prints the second element of the numbers list

numbers[1] = 50.5                   #Changes the second element of the numbers list to 50.5
print(numbers[1])                   #Prints the second element of the numbers list again

numbers[2] = "Seven"                #Changes the third element of the numbers list to "Seven"
print(numbers[2])                   #Prints the third element of the numbers list

print(numbers)                      #Prints the list (in list form)



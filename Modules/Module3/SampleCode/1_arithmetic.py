apples = 15                                         #Declares a variable named apples and assigns it the value 15

oranges = 13                                        #Declares a variable named oranges and assigns it the value 13

total = apples + oranges                            #Declares a variable named total and assigns to it the sum of apples and oranges.

print("Total:", total)                              #Prints the value of the total variable


print()


total_tasks = 20                                    #Declares a variable named total_tasks and assigns it the value 20

completed_tasks = 12                                #Declares a variable named completed_tasks and assigns it the value 12

tasks_remaining = total_tasks - completed_tasks     #Declares a variable named task_remaining and assigns to it the difference of total_tasks and completed_tasks.

print("Tasks remaining:", tasks_remaining)          #Prints the value of the tasks_remaining variable


print()


total_minutes = 15                                  #Declares a variable named total_minutes and assigns it the value 15

seconds_in_a_minute = 60                            #Declares a variable named seconds_in_a_minute and assigns it the value 60

total_seconds = total_minutes * seconds_in_a_minute #Declares a variable named total_econds and assigns to it the product of total_minutes and seconds_in_a_minute.

print("Total seconds:", total_seconds)              #Prints the value of the total_seconds variable


print()


distance = 120.6                                    #Declares a variable named distance and assigns it the value 20.6


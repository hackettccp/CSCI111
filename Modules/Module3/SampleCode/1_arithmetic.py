speed1 = distance / time                            #Declares a variable named speed1 and assigns to it the quotient of distance divided by time.

print("Speed 1:", speed1)                           #Prints the value of the speed1 variable

speed2 = distance // time                           #Declares a variable named speed2 and assigns to it the quotient of distance divided by time.

print("Speed 2:", speed2)                           #Prints the value of the speed2 variable


print()


total_items = 77                                    #Declares a variable named total_items and assigns it the value 77

total_people = 28                                   #Declares a variable named total_people and assigns it the value 32

items_per_person = total_items // total_people      #Declares a variable named items_per_person and assigns to it the quotient of total_items and total_people.

print("Items per person:", items_per_person)        #Prints the value of the items_per_person variable

leftover_items = total_items % total_people         #Declares a variable named leftover_items and assigns to it the result of total_items mod total_people.

print("Leftover items:", leftover_items)            #Prints the value of the leftover_items variable


print()



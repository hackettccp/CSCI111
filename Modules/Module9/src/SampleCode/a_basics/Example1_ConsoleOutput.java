package SampleCode.a_basics;

/**
 * Demonstrates printing console output using
 * the System object.
 */
class Example1_ConsoleOutput {
    
    /**
     * Main Method. This is where a Java program begins.
     */
    public static void main(String[] args) {
        System.out.println(10);             //Prints 10
        System.out.println();               //Prints blank line
        System.out.println(20);             //Prints 20
        System.out.println(30);             //Prints 30
        System.out.println(40);             //Prints 40
        System.out.println();               //Prints blank line

        System.out.print(100);              //Prints 100
        System.out.print(200);              //Prints 200
        System.out.print(300);              //Prints 300
        System.out.print(400);              //Prints 400
    }
    
}

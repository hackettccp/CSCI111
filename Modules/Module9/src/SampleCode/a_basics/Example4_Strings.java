package SampleCode.a_basics;

/**
 * Demonstrates String declaration and initialization.
 */
class Example4_Strings {

    /**
     * Main Method. This is where the program begins.
     */
    public static void main(String[] args) {
        String myGreeting;                      //Declares a String variable named myGreeting
        myGreeting = "Hello World!";            //Initializes myGreeting with the String literal "Hello World"
        System.out.println(myGreeting);         //Prints the value of myGreeting
        myGreeting = "Hi there!";               //Reassigns the the String literal "Hi There" to the myGreeting variable
        System.out.println(myGreeting);         //Prints the value of myGreeting
    }

}
